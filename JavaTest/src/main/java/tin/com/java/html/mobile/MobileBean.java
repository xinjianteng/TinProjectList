package tin.com.java.html.mobile;

public class MobileBean {

    private String terminalFactory;//终端厂商
    private String terminalModel;//手机型号
    private String terminalName;//终端名称


    public String getTerminalFactory() {
        return terminalFactory;
    }

    public void setTerminalFactory(String terminalFactory) {
        this.terminalFactory = terminalFactory;
    }

    public String getTerminalModel() {
        return terminalModel;
    }

    public void setTerminalModel(String terminalModel) {
        this.terminalModel = terminalModel;
    }

    public String getTerminalName() {
        return terminalName;
    }

    public void setTerminalName(String terminalName) {
        this.terminalName = terminalName;
    }


}
